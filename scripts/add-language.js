// Scaffolds a new language file in src/locales/ with all English keys set to empty strings.
// Translators then fill in the values.
//
// Usage: node scripts/add-language.js <locale>
// Example: node scripts/add-language.js fr
//          node scripts/add-language.js zh-CN

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const srcDir = path.join(__dirname, '../src');
const localesDir = path.join(srcDir, 'locales');

const locale = process.argv[2];

if (!locale) {
  console.error('Usage: node scripts/add-language.js <locale>');
  console.error('Example: node scripts/add-language.js fr');
  process.exit(1);
}

if (locale === 'en') {
  console.error('Cannot create language-en.json — it is auto-generated by sync.cjs.');
  process.exit(1);
}

const enPath = path.join(srcDir, 'language-en.json');
if (!fs.existsSync(enPath)) {
  console.error('language-en.json not found. Run sync first.');
  process.exit(1);
}

fs.mkdirSync(localesDir, { recursive: true });

const outPath = path.join(localesDir, `language-${locale}.json`);
if (fs.existsSync(outPath)) {
  console.error(`language-${locale}.json already exists.`);
  process.exit(1);
}

// Recursively replace every leaf string with ""
function emptyValues(obj) {
  if (typeof obj !== 'object' || obj === null || Array.isArray(obj)) {
    return '';
  }
  return Object.fromEntries(Object.entries(obj).map(([k, v]) => [k, emptyValues(v)]));
}

const enData = JSON.parse(fs.readFileSync(enPath, 'utf8'));
const scaffold = emptyValues(enData);

fs.writeFileSync(outPath, JSON.stringify(scaffold, null, 2));

console.log(`Created src/locales/language-${locale}.json`);
console.log(`\nNext steps:`);
console.log(`  1. Fill in the empty string values in src/locales/language-${locale}.json`);
console.log(`  2. Commit and push the file`);
console.log(`  3. Future syncs will automatically keep this file in step with English`);
